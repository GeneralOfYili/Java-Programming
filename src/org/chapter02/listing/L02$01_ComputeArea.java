package org.chapter02.listing;

public class L02$01_ComputeArea {

	public static void main(String[] args) {
		//Declare radius
		double radius;
		//Declare area
		double area;
		
		//Assign a radius
		radius = 20;
		area = radius * radius * 3.14159;
		
		//Display results
		System.out.println("The area for the circle of radius "+radius+" is "+area);

	}

}
